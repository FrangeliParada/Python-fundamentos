# OPERADORES mas comunes
print(3 + 4)
print(3 - 4)
print(3 / 4)
print(3 * 4)
print(5 % 4)
print(10 // 3)
print(10 ** 3)

my_float = 2.5 * 2
print("Hola " * int(my_float))

# OPERADORES ARISMÉTICOS

print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(3 <= 4)
print(3 == 4)
print(3 != 4)

# OPERADORES LÓGICOS (and, or, not)

print(3 > 4 and "Hola" > "Python")
print(3 > 4 or "Hola" > "Python")
print(3 < 4 and "Hola" < "Python")
print(3 < 4 or "Hola" > "Python")
print(3 < 4 or  ("Hola" > "Python" and 4==4))
print(not(3 > 4))

